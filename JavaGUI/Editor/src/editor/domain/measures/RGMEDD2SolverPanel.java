    @Override
    public void initializeFor(SolverParams _params, NetPage page, NetPage evalPage, 
                              boolean simplifiedUI, MeasurePage measPage) 
    {
        initializing = true;
        
        params = (RGMEDD2SolverParams)Util.deepCopy(_params);
        
        checkBox_counterExamples.setSelected(params.genCounterExamples);
        comboBox_varOrder.setSelectedItem(params.varOrder);
        
        // Dependency on the requested formulas
        boolean hasCTL = false;
        for (AbstractMeasure am : measPage.measures) {
            if (am instanceof FormulaMeasure) {
                FormulaMeasure fm = (FormulaMeasure)am;
                switch (fm.getLanguage()) {
                    case CTL:
                    case LTL:
                    case CTLSTAR:
                        hasCTL = true;
                        break;
                }
            }
        }
        checkBox_counterExamples.setVisible(hasCTL);

        initializing = false;
    }

    @Override
    public void deinitialize() {
        params = null;
    }
    
    @Override
    public SolverParams currParams() {
        return params;
    }

    @Override
    public boolean areParamsCorrect() {
        return true;
    }
    
    

